class TestV1FreezeRespected:
    """Verify our campaign did not touch frozen V1 artifacts."""

    def test_dashboard_shared_js_untouched(self):
        # dashboard-shared.js's exported atom list at line 510 must still
        # match what V1 + proforma-v2 + dashboard-v2 (root) expect.
        content = (STATIC_DIR / "dashboard-shared.js").read_text()
        assert "window.EstrellaShared = Object.freeze({" in content
        for atom in ("Badge", "Card", "Btn", "Sel", "Toast",
                     "SessionBanner", "EstrellaMark", "SubTabStrip",
                     "Sidebar", "StatusDot", "GateBlock", "SectionHeader",
                     "CompactTable", "EmptyState"):
            assert atom in content, \
                f"dashboard-shared.js export contract drift: missing {atom}"

    def test_v1_dashboard_html_present_and_large(self):
        """V1 dashboard.html is the 20K-LOC monolith. If our campaign
        accidentally rewrote it, this size assertion catches it."""
        f = STATIC_DIR / "dashboard.html"
        assert f.is_file()
        # Lower bound = 15,000 lines (V1 inventory at audit time was
        # ~20,287 lines; allow shrinkage if someone legitimately trims it,
        # but not a wholesale rewrite).
        lines = f.read_text().count("\n")
        assert lines >= 15000, \
            f"dashboard.html shrank from ~20K LOC to {lines} — likely modified"

    def test_live_proforma_v2_present(self):
        """The operational /dashboard/proforma-v2.html (797 LOC at audit time)
        must remain accessible. Atlas's stub at /dashboard/atlas/proforma-v2.html
        is DIFFERENT — operators still use the live one."""
        live = STATIC_DIR / "proforma-v2.html"
        atlas = ATLAS_DIR / "proforma-v2.html"
        assert live.is_file()
        assert atlas.is_file()
        # They must be distinct files
        assert live.read_text() != atlas.read_text(), \
            "atlas/proforma-v2.html accidentally identical to live page"


# ─────────────────────────────────────────────────────────────────────────────
# Authority discipline — Atlas pages must not invent business state
# ─────────────────────────────────────────────────────────────────────────────

class TestAuthorityDiscipline:
    """Lesson F + Lesson I: frontend must not invent business truth.
    These checks scan for forbidden patterns."""

    @pytest.mark.parametrize("page", ATLAS_PAGES)
    def test_no_local_storage_writes(self, page):
        """Atlas pages must not stash business state in localStorage —
        backend is the only source of truth."""
        content = (ATLAS_DIR / page).read_text()
        forbidden = ("localStorage.setItem", "sessionStorage.setItem")
        for token in forbidden:
            assert token not in content, \
                f"{page} contains forbidden frontend persistence: {token}"

    @pytest.mark.parametrize("page", ATLAS_PAGES)
    def test_no_write_methods_to_authority_routes(self, page):
        """Phase One is read-only. PUT/POST/DELETE to authority routes
        is forbidden in any atlas/*.html page."""
        content = (ATLAS_DIR / page).read_text()
        # Match `method: 'POST'` etc. patterns in fetch options
        write_patterns = [
            r"method:\s*['\"]POST['\"]",
            r"method:\s*['\"]PUT['\"]",
            r"method:\s*['\"]DELETE['\"]",
            r"method:\s*['\"]PATCH['\"]",
        ]
        for pat in write_patterns:
            matches = re.findall(pat, content)
            assert not matches, \
                f"{page} contains write-method fetch call: {pat}"


# ─────────────────────────────────────────────────────────────────────────────
# atlas-shared.js Lesson F discipline — no domain knowledge
# ─────────────────────────────────────────────────────────────────────────────

class TestAtlasSharedLessonF:
    """atlas-shared.js is the new visual-atom library for Atlas V2.
    It MUST NOT learn domain concepts (shipment state, customs rules,
    PZ readiness, wFirma semantics)."""

    def test_atlas_shared_has_no_domain_tokens(self):
        content = (ATLAS_DIR / "atlas-shared.js").read_text()
        domain_forbidden = [
            "clearance_status", "sad_status", "pz_status", "tracking_status_key",
            "wfirma_correction_push_allowed", "pz_correction_lifecycle_enabled",
            "OPERATOR_REVIEWED", "STAGED_CORRECTION",
            "_derive_status", "_derive_clearance",
        ]
        for tok in domain_forbidden:
            assert tok not in content, \
                f"atlas-shared.js contains domain token {tok!r} — " \
                f"Lesson F violation (visual atoms only)"

    def test_atlas_shared_has_no_fetch_calls(self):
        """Visual atoms do not fetch. They render what consumers pass in."""
        content = (ATLAS_DIR / "atlas-shared.js").read_text()
        assert "fetch(" not in content
        assert "XMLHttpRequest" not in content


