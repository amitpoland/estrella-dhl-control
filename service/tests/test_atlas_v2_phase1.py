class TestAtlasFilesExist:
    def test_atlas_directory_exists(self):
        assert ATLAS_DIR.is_dir(), f"Atlas directory missing at {ATLAS_DIR}"

    def test_atlas_shared_js_exists(self):
        f = ATLAS_DIR / "atlas-shared.js"
        assert f.is_file(), "atlas-shared.js missing"
        content = f.read_text()
        # Must surface AtlasShared on window
        assert "window.AtlasShared" in content
        # Required atoms
        for atom in ("PageHeader", "NavStrip", "Btn", "Badge", "Kpi",
                     "Empty", "Loading", "ErrorBanner", "TierBanner"):
            assert atom in content, f"atlas-shared.js missing {atom}"

    @pytest.mark.parametrize("page", ATLAS_PAGES)
    def test_page_exists(self, page):
        f = ATLAS_DIR / page
        assert f.is_file(), f"Atlas page missing: {page}"
        content = f.read_text()
        # Must load atlas-shared.js
        assert "/dashboard/atlas/atlas-shared.js" in content, \
            f"{page} does not load atlas-shared.js"
        # Must mount React root
        assert 'id="root"' in content
        # Must use React UMD + Babel CDN
        assert "react@18" in content
        assert "babel" in content.lower()


