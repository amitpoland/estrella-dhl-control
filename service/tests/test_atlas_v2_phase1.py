# ─────────────────────────────────────────────────────────────────────────────
# Disabled buttons preserved with title tooltips (no buttons removed)
# ─────────────────────────────────────────────────────────────────────────────

class TestDisabledButtonsHaveTooltips:
    """Every disabled button on Phase One pages must carry a `title=` tooltip
    explaining why. Otherwise the button is a dead end with no operator hint."""

    @pytest.mark.parametrize("page", ATLAS_PAGES)
    def test_disabled_buttons_have_titles(self, page):
        content = (ATLAS_DIR / page).read_text()
        # Find every `disabled` button declaration and ensure it has `title=`
        # within the same component invocation (rough heuristic — count
        # `disabled` keyword and `title=` near it).
        disabled_count = len(re.findall(r"\bdisabled\b", content))
        title_count    = len(re.findall(r"\btitle\s*=", content))
        # We don't require title-per-disabled 1:1 (some titles are on the
        # parent <Btn>, some on <button>), but title count should be
        # at least 50% of disabled count to catch a refactor that drops
        # tooltips entirely.
        if disabled_count >= 2:
            assert title_count >= disabled_count // 2, (
                f"{page}: {disabled_count} disabled occurrences but only "
                f"{title_count} title= attributes — tooltips missing"
            )


